// Componente reutilizable para manejar la paginaciÃ³n, se puede usar en cualquier web app
import { ChevronLeft, ChevronRight } from "lucide-react";
import { Button } from "../ui/button";
import { useSearchParams } from "react-router";

interface Props {
  totalPages: number;
}

export const CustomPagination = ({ totalPages }: Props) => {
  const [searchParams, setSearchParams] = useSearchParams();

  const queryPage = searchParams.get("page") ?? "1";
  const page = isNaN(parseInt(queryPage)) ? 1 : parseInt(queryPage);

  const handlePageChange = (page: number) => {
    if (page < 1 || page > totalPages) return;

    // searchParams no es un objeto normal, es uno de tipo URLSearchParams
    // por eso necesitamos modificar el valor y luego sobreescribirlo con el setSearchParams
    searchParams.set("page", page.toString());
    setSearchParams(searchParams);

    // alternativa
    // setSearchParams((prev) => {
    //   prev.set("page", page.toString());
    //   return prev;
    // });
  };

  return (
    <div className="flex items-center justify-center space-x-2">
      <Button
        variant="outline"
        size="sm"
        disabled={page === 1}
        onClick={() => handlePageChange(page - 1)}
      >
        <ChevronLeft className="h-4 w-4" />
        Previous
      </Button>

      {Array.from({ length: totalPages }).map((_, index) => (
        <Button
          variant={page === index + 1 ? "default" : "outline"}
          size="sm"
          key={index}
          onClick={() => handlePageChange(index + 1)}
        >
          {index + 1}
        </Button>
      ))}

      <Button
        variant="outline"
        size="sm"
        disabled={page === totalPages}
        onClick={() => handlePageChange(page + 1)}
      >
        Next
        <ChevronRight className="h-4 w-4" />
      </Button>
    </div>
  );
};
